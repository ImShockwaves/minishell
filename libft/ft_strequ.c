#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int	cpt;

	cpt = 0;
	if (!(s1) || !(s2))
		return (0);
	while (s1[cpt] == s2[cpt] && s1 && s2)
	{
		if (s1[cpt] == '\0' && s2[cpt] == '\0')
			return (1);
		cpt++;
	}
	return (0);
}

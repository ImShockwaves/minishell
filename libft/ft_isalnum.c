#include "libft.h"

int		ft_isalnum(int str)
{
	if (str < 'a' || str > 'z')
	{
		if (str < 'A' || str > 'Z')
		{
			if (str > '9' || str < '0')
				return (0);
		}
	}
	return (1);
}

int		ft_sqrt(int nb)
{
	int cpt;

	cpt = 1;
	if (nb <= 0)
		return (0);
	while (cpt <= nb / 2)
	{
		if (cpt * cpt == nb)
			return (cpt);
		cpt++;
	}
	return (0);
}

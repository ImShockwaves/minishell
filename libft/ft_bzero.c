#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	cpt;
	char	*tmp;

	cpt = 0;
	tmp = s;
	while (cpt < n && n > 0)
	{
		tmp[cpt] = 0;
		cpt++;
	}
	return (0);
}
